import AppProviders from "@/providers/AppProviders";
import type { Metadata } from "next";
import { Cinzel_Decorative, Manrope } from "next/font/google";
import NextTopLoader from "nextjs-toploader";

import { auth } from "@/auth";
import Footer from "@/components/shared/Footer/Footer";
import Navbar from "@/components/shared/section/navbar/navbar";
import { cn } from "@/lib/utils";
import { ReactNode } from "react";
import { Toaster } from "sonner";
import TopbarCarousel from "../components/shared/section/topbar/topbar";
import "./globals.css";

const cinzel = Cinzel_Decorative({
  subsets: ["latin"],
  variable: "--font-cinzel",
  weight: ["400", "700", "900"],
  display: "swap",
});

const manrope = Manrope({
  subsets: ["latin"],
  variable: "--font-manrope",
  weight: ["400", "500", "600", "700", "800"],
});

export const metadata: Metadata = {
  title: "Grand Purp",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: ReactNode;
}>) {
  const session = await auth();

  return (
    <html lang="en">
      <body
        className={cn(
          cinzel.variable,
          manrope.className,
          "antialiased bg-[#F0F8FF]"
        )}
      >
        <TopbarCarousel />
        <Navbar isLoggedin={!!session} />
        <AppProviders>{children}</AppProviders>
        <Footer />
        <Toaster position="bottom-right" richColors />
        <NextTopLoader showSpinner={false} color="#1E2A38" />
      </body>
    </html>
  );
}
